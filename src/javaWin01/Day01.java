package javaWin01;

public class Day01 {

	public static void main(String[] args) {
		int a=0;							//선언문 : 변수 이름이 a, 초기값 0
		int b=0;							//선언문 : 변수 이름이 b, 초기값 0
		a=30;								//30을 변수 a에 대입
		b=50;								//50을 변수 a에 대입
		int c=0;							//선언문 : 변수 이름이 c, 초기값 0
		c=a+b;								//a+b를 변수 c에 대입
		System.out.println(c);				//c 변수값을 콘솔에 출력
				
		
		//System.out.println() 명령어 : 콘솔에 출력하라
	}
	
}
